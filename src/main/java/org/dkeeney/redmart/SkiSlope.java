package org.dkeeney.redmart;

public class SkiSlope {
    private int x;
    private int y;
    private int height;

    public SkiSlope(int x, int y, int height) {
        this.x = x;
        this.y = y;
        this.height = height;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getHeight() {
        return height;
    }
}
